export const t = (key: string, language: "en" | "ar"): string => {
  return (
    translations[language][key as keyof (typeof translations)["en"]] || key
  );
};

// Convert English numbers to Arabic-Indic numerals
export const convertToArabicNumerals = (text: string): string => {
  const arabicNumerals = ["٠", "١", "٢", "٣", "٤", "٥", "٦", "٧", "٨", "٩"];
  return text.replace(/[0-9]/g, (digit) => arabicNumerals[parseInt(digit)]);
};

// Format numbers based on language
export const formatNumber = (
  number: string | number,
  language: "en" | "ar"
): string => {
  const numStr = number.toString();
  return language === "ar" ? convertToArabicNumerals(numStr) : numStr;
};

// Format phone numbers based on language
export const formatPhoneNumber = (
  phoneNumber: string,
  language: "en" | "ar"
): string => {
  return language === "ar" ? convertToArabicNumerals(phoneNumber) : phoneNumber;
};
